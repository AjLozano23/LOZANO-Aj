// Activity 4 Comparison or Relational Operators
let firstFavNumb = 23; //first favorite number
let secondFavNumb = 32; //second favorite number

console.log(firstFavNumb > secondFavNumb); //false
console.log(firstFavNumb < secondFavNumb); //true
console.log(firstFavNumb >= secondFavNumb); // false
console.log(firstFavNumb <= secondFavNumb); // true
console.log(firstFavNumb === secondFavNumb); //false
console.log(firstFavNumb == secondFavNumb); //false
console.log(firstFavNumb !== secondFavNumb); //true
console.log(firstFavNumb != secondFavNumb); //true